                                   properties[j] is RootProperty, "Checking property " + j);
                    }
                    if (isDirectory[j])
                    {
                        Assert.IsTrue(
                                   properties[j] is DirectoryProperty, "Checking property " + j);
                    }
                    if (isDocument[j])
                    {
                        Assert.IsTrue(
                                   (properties[j] is DocumentProperty), "Checking property " + j);
                    }
                    Assert.AreEqual(names[j],
                                 ((Property)properties[j]).Name, "Checking property " + j);
                }
            }
